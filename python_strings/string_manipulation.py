b = "Hello, World"
print(b[2:5])
print(b[:5]) #slice from the start to index 5 (5 not ncluded)
print(b[7:]) #slice from 7 to the end
print(b[-5:])
print(b[-5:-1]) #slice from -5 to -1 (not included)
print(b[::2]) #slice with a step of 2
print(b[::-1]) #reverse the string
print(b[1:10:2]) #slice from 1 to 10 with a step of 